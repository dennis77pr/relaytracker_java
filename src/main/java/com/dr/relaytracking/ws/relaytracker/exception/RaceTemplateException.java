package com.dr.relaytracking.ws.relaytracker.exception;

public class RaceTemplateException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6674051954813929549L;

	public RaceTemplateException() {

	}

	public RaceTemplateException(String message) {
		super(message);

	}

	public RaceTemplateException(Throwable cause) {
		super(cause);
	}

	public RaceTemplateException(String message, Throwable cause) {
		super(message, cause);
	}

	public RaceTemplateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
